user_input = input("Enter a number as a string: ")

integer_number = int(user_input)
float_number = float(user_input)
string_number = str(user_input)

print("Integer ", integer_number)
print("The type of value is ", (type(integer_number)))

print("Float ", float_number)
print("The type of value is ", (type(float_number)))

print("String", string_number)
print("The type of value is ", (type(string_number)))